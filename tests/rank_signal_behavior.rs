//! Parity tests — locks in current `search_files` ranking behavior.
//!
//! These tests MUST pass against current `main` *before* the `RankSignal`
//! trait refactor lands. After the refactor, they must still pass
//! byte-identically — same tiers, same ordering, same ancillary lines. That
//! invariant is the refactor's acceptance bar (CONTEXT.md §Acceptance bar).
//!
//! ## Update workflow
//!
//! The golden strings below are generated by running this test with
//! `SYMFORGE_UPDATE_RANK_GOLDEN=1`. The run will fail with a diff-friendly
//! `println!` dump of the actual output for each query; the maintainer
//! pastes the dump into the `expected` column. Subsequent runs assert
//! byte-identical equality.
//!
//! Dumps include the full tool return string (envelope + ranked body) so
//! that *any* drift — reordering, tier re-labeling, evidence-line changes,
//! new hints — is caught.

